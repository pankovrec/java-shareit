drop table if exists users, requests, items, booking, comments;

CREATE TABLE IF NOT EXISTS users
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(50) UNIQUE NOT NULL,
    name VARCHAR(50) NOT NULL
    );

CREATE TABLE IF NOT EXISTS requests
(
    id BIGSERIAL CONSTRAINT requests_pk PRIMARY KEY,
    description VARCHAR(1024) NOT NULL,
    requester_id BIGINT NOT NULL CONSTRAINT requests_requester_id REFERENCES users ON UPDATE CASCADE ON DELETE CASCADE,
    created TIMESTAMP WITHOUT TIME ZONE
);

CREATE TABLE IF NOT EXISTS items
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(1000),
    available BOOLEAN,
    owner_id BIGINT NOT NULL CONSTRAINT items_owner_id_fk REFERENCES users(id) ON DELETE CASCADE,
    request_id BIGINT
    );



CREATE TABLE IF NOT EXISTS booking
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_date TIMESTAMP WITHOUT TIME ZONE,
    end_date TIMESTAMP WITHOUT TIME ZONE,
    item_id BIGINT NOT NULL CONSTRAINT  booking_item_id REFERENCES items(id) ON DELETE CASCADE,
    booker_id BIGINT NOT NULL CONSTRAINT booking_user_id REFERENCES users(id) ON DELETE CASCADE,
    status VARCHAR(50) NOT NULL
    );

CREATE TABLE IF NOT EXISTS comments
(
    id BIGSERIAL CONSTRAINT comments_pk PRIMARY KEY,
    text VARCHAR(1000) NOT NULL,
    item_id BIGINT NOT NULL CONSTRAINT comments_item_id REFERENCES items ON UPDATE CASCADE ON DELETE CASCADE,
    author_id BIGINT NOT NULL CONSTRAINT comments_author_id REFERENCES users ON UPDATE CASCADE ON DELETE CASCADE,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL
    );